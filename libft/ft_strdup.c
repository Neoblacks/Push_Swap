#include "libft.h"

char	*ft_strdup(const char *src)
{
	size_t	i;
	char	*str;

	if (!src)
		return (NULL);
	str = malloc(sizeof(const char) * (ft_strlen(src) + 1));
	if (!str)
		return (NULL);
	i = 0;
	while (src[i])
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
